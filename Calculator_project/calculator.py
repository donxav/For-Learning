
def add(x,y):
    return x+y #define addition as a function

def sub(x,y):
    return x-y #define substraction as a function

def multi(x,y):
    return x * y #define multiplication as a function

def div(x,y):
    if y==0:
        print('Division by zero is not possible')
    else:
        return x/y #define division as a function